            <h2>Ziele und Prinzipien</h2>
            <p>
                Die Oeno-Danubia beruht auf den Prinzipien <span class="italic font-semibold">religio, amicitia,
                    scientia</span> und <span class="italic font-semibold">patria.</span> Sie will eine
                lebenslange
                Freundschaft begründen, welche sich durch ihre couleurstudentischen Traditionen und Comment getragen
                weiß.
            </p>
            <ul>
                <li>
                    <p>
                        <span class="font-bold">Religio</span> heißt gemeinsames Bekenntnis zum römisch-katholischen
                        Glauben, die Bereitschaft zu dessen
                        Verwirklichung im täglichen Leben und Verantwortung in der Welt auf Basis unseres christlichen
                        Wertefundaments zu übernehmen.
                    </p>
                </li>
                <li>
                    <p>
                        <span class="font-bold">Amicitia</span> ein Leben im freundschaftlichen Geiste der Gemeinschaft
                        und
                        der gegenseitigen Hilfe, sowie die
                        Pflege eines wahren, über die eigene Studienzeit und die eigene Generation hinausreichenden
                        Lebensbundes.
                    </p>
                </li>
                <li>
                    <p>
                        <span class="font-bold">Scientia</span> meint das erfolgreiche Abschließen des Studiums und die
                        ernsthafte Auseinandersetzung mit wissenschaftlichen
                        Fragen über den Horizont des eigenen Fachstudiums hinaus.
                    </p>
                </li>
                <li>
                    <p>
                        <span class="font-bold">Patria</span> bedeutet engagiertes Eintreten für Recht, Freiheit und
                        Toleranz in unserem demokratischen Gemeinwesen,
                        getragen von der Liebe zu unserem deutschen Vaterland und entschlossen zur Verwirklichung von
                        Frieden und
                        Verbundenheit in Europa und auf der ganzen Welt.
                    </p>
                </li>
            </ul>
            <p>
                Zusammen mit über 120 anderen Verbindungen ist die Oeno-Danubia unter dem Dach des 1856 gegründeten
